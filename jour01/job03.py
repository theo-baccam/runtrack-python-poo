class Operation:
    def __init__(self):
        self.nombre1 = 83
        self.nombre2 = 21

    # On crée une nouvelle méthode qui s'apelle addition, et qui return la somme des nombres
    def addition(self):
        somme = self.nombre1 + self.nombre2
        return somme


op = Operation()

# Cette fois on imprime le resultat de la méthode addition()
resultat = op.addition()
print(resultat)
